# Swapping using temp variable
a = 4
b = 6

temp = a
a = b
b = temp

print("After swapping (using temp):")
print("a =", a)
print("b =", b)

# Swapping using Python shortcut
x = 10
y = 20

x, y = y, x

print("After swapping (Python method):")
print("x =", x)
print("y =", y)
